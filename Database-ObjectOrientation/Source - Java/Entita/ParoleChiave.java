package Entita;
import java.util.List;

public class ParoleChiave {
	
	private String parola;
	private List<ParoleLezione> paroleDiLezione;

	//Getter e setter
	public String getParola() {
		return parola;
	}

	public void setParola(String parola) {
		this.parola = parola;
	}

	public List<ParoleLezione> getParoleDiLezione() {
		return paroleDiLezione;
	}

	public void setParoleDiLezione(List<ParoleLezione> paroleDiLezione) {
		this.paroleDiLezione = paroleDiLezione;
	}
	
	
	
}
